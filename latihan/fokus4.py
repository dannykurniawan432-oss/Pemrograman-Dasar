# operasi aritmatika dasar

a = 10
b = 5

#operasi penjumlahan
hasil = a + b
print(a, '+ ', b, ' = ', hasil)

#operasi pengurangan
hasil = a - b
print(a, '- ', b, ' = ', hasil)

#operasi perkalian
hasil = a * b
print(a, '* ', b, ' = ', hasil)

#operasi pembagian
hasil = a / b
print(a, '/ ', b, ' = ', hasil)

#operasi modulus
hasil = a % b
print(a, '% ', b, ' = ', hasil)

#operasi eksponen
hasil = a ** b
print(a, '** ', b, ' = ', hasil)

#operasi floor division
hasil = a // b  
print(a, '// ', b, ' = ', hasil)

# prioritas operasi
x = 10               
y = 3
z = 4

# contoh penggunaan
hasil = x + y * z
print(x, '+', y, '*', z, '=', hasil)

hasil = (x + y) * z
print('(', x, '+', y, ')*', z, '=', hasil)

hasil = x ** y + z
print(x, '**', y, '+', z, '=', hasil)

hasil = x ** (y + z)
print(x, '**(', y, '+', z,') =', hasil)

hasil = x + y // z
print(x, '+', y, '//', z, '=', hasil)

hasil = (x + y) // z
print('(', x, '+', y, ')//', z, '=', hasil)

hasil = x + y % z
print(x, '+', y, '%', z, '=', hasil)

hasil = (x + y) % z
print('(', x, '+', y, ')%', z, '=', hasil)

hasil = x * y - z
print(x, '*', y, '-', z, '=', hasil)

hasil = x * (y - z)
print(x, '*(', y, '-', z,') =', hasil)

hasil = x / y + z
print(x, '/', y, '+', z, '=', hasil)

hasil = x / (y + z)
print(x, '/(', y, '+', z,') =', hasil)

hasil = x - y + z
print(x, '-', y, '+', z, '=', hasil)

hasil = x - (y + z)
print(x, '-(', y, '+', z,') =', hasil)

hasil = x % y * z
print(x, '%', y, '*', z, '=', hasil)

hasil = (x % y) * z
print('(', x, '%', y, ')*', z, '=', hasil)

hasil = x // y + z
print(x, '//', y, '+', z, '=', hasil)

hasil = (x // y) + z
print('(', x, '//', y, ')+', z, '=', hasil)

hasil = x ** y // z
print(x, '**', y, '//', z, '=', hasil)

hasil = (x ** y) // z
print('(', x, '**', y, ')//', z, '=', hasil)

hasil = x ** (y // z)
print(x, '**(', y, '//', z,') =', hasil)
